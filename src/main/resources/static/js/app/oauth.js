function onHover()
{
    $("#btn-google").attr('src', '/images/google/btn_google_signin_dark_focus_web.png');
}

function offHover()
{
    $("#btn-google").attr('src', '/images/google/btn_google_signin_dark_normal_web.png');
}

function onClick()
{
    $("#btn-google").attr('src', '/images/google/btn_google_signin_dark_pressed_web.png');
}